# Día 2 - Variables, Tipos de datos y Operaciones

# 1. Variables con diferentes tipos de datos
nombre = "Juan Forero"  # String (texto)
edad = 38               # Integer (entero)
altura = 1.75           # Float (decimal)
es_estudiante = True    # Boolean (verdadero/falso)

# 2. Mostrar el tipo de cada variable
print("El tipo de nombre es:", type(nombre))
print("El tipo de edad es:", type(edad))
print("El tipo de altura es:", type(altura))
print("El tipo de es_estudiante es:", type(es_estudiante))

# 3. Operaciones con números
a = 15
b = 4

print("\nSuma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)
print("División entera:", a // b)
print("Módulo (resto):", a % b)
